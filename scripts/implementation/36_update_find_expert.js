        var widgetId = widgetGR.getUniqueValue();
        var widgetName = widgetGR.getValue('name');
        gs.info('[Skills Hub] Found widget: ' + widgetName + ' sys_id=' + widgetId);

        // ============================================================
        // 1. Replace SERVER SCRIPT
        // ============================================================
        var newServer = '(function() {\n'
            + '  var SCORE_MUST_HAVE = 20;\n'
            + '  var SCORE_NICE_HAVE = 5;\n'
            + '  var MULTIPLIER_EXPERT = 1.5;\n'
            + '  var MULTIPLIER_ADVANCED = 1.2;\n'
            + '\n'
            + '  // --- SEARCH SKILLS (typeahead, deduplicated by name) ---\n'
            + '  if (input && input.action == "searchSkills") {\n'
            + '      var term = (input.term || "").toLowerCase();\n'
            + '      var seen = {};\n'
            + '      var results = [];\n'
            + '      if (term.length >= 2) {\n'
            + '          var sg = new GlideRecord("cmn_skill");\n'
            + '          sg.addQuery("name", "CONTAINS", term);\n'
            + '          sg.orderBy("name");\n'
            + '          sg.setLimit(200);\n'
            + '          sg.query();\n'
            + '          while (sg.next()) {\n'
            + '              var sName = sg.getValue("name");\n'
            + '              if (!seen[sName]) {\n'
            + '                  seen[sName] = true;\n'
            + '                  results.push(sName);\n'
            + '              }\n'
            + '          }\n'
            + '      }\n'
            + '      data.skillSuggestions = results;\n'
            + '  }\n'
            + '\n'
            + '  // --- Helper: resolve category path for a cmn_skill ---\n'
            + '  function getCategoryPath(skillSysId) {\n'
            + '      var m2m = new GlideRecord("cmn_skill_m2m_category");\n'
            + '      m2m.addQuery("skill", skillSysId);\n'
            + '      m2m.setLimit(1);\n'
            + '      m2m.query();\n'
            + '      if (m2m.next()) {\n'
            + '          var cId = m2m.getValue("category");\n'
            + '          var cGR = new GlideRecord("cmn_skill_category");\n'
            + '          if (cGR.get(cId)) {\n'
            + '              var path = cGR.getValue("name");\n'
            + '              var pRef = cGR.getValue("parent");\n'
            + '              var d = 0;\n'
            + '              while (pRef && d < 5) {\n'
            + '                  var pGR = new GlideRecord("cmn_skill_category");\n'
            + '                  if (pGR.get(pRef)) {\n'
            + '                      path = pGR.getValue("name") + " > " + path;\n'
            + '                      pRef = pGR.getValue("parent");\n'
            + '                  } else break;\n'
            + '                  d++;\n'
            + '              }\n'
            + '              return path;\n'
            + '          }\n'
            + '      }\n'
            + '      return "";\n'
            + '  }\n'
            + '\n'
            + '  // --- Helper: parse proficiency level to number ---\n'
            + '  function levelToInt(levelStr) {\n'
            + '      if (levelStr.indexOf("Expert") > -1) return 5;\n'
            + '      if (levelStr.indexOf("Advanced") > -1) return 4;\n'
            + '      if (levelStr.indexOf("Proficient") > -1) return 3;\n'
            + '      if (levelStr.indexOf("Intermediate") > -1 || levelStr.indexOf("Learner") > -1) return 2;\n'
            + '      return 1;\n'
            + '  }\n'
            + '\n'
            + '  // --- SEARCH EXPERTS BY SKILL NAMES ---\n'
            + '  if (input && input.action == "search") {\n'
            + '      var mustNames = input.mustHave ? input.mustHave.split(",") : [];\n'
            + '      var niceNames = input.niceHave ? input.niceHave.split(",") : [];\n'
            + '      var allNames = mustNames.concat(niceNames);\n'
            + '\n'
            + '      // Find ALL cmn_skill sys_ids for each skill name\n'
            + '      var nameToIds = {};\n'
            + '      if (allNames.length > 0) {\n'
            + '          var skillGR = new GlideRecord("cmn_skill");\n'
            + '          skillGR.addQuery("name", "IN", allNames.join(","));\n'
            + '          skillGR.query();\n'
