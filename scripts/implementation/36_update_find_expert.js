            + '      if (maxPossible == 0) maxPossible = 100;\n'
            + '\n'
            + '      for (var id in users) {\n'
            + '          var u = users[id];\n'
            + '          var skills = [];\n'
            + '          u.rawScore = 0;\n'
            + '\n'
            + '          for (var sName in u.skillGroups) {\n'
            + '              var sg = u.skillGroups[sName];\n'
            + '              // Average proficiency across categories\n'
            + '              var totalLevel = 0;\n'
            + '              for (var e = 0; e < sg.entries.length; e++) {\n'
            + '                  totalLevel += sg.entries[e].level_value;\n'
            + '              }\n'
            + '              var avgLevel = totalLevel / sg.entries.length;\n'
            + '\n'
            + '              // Score based on averaged level\n'
            + '              var points = sg.isMust ? SCORE_MUST_HAVE : SCORE_NICE_HAVE;\n'
            + '              if (avgLevel >= 5) points *= MULTIPLIER_EXPERT;\n'
            + '              else if (avgLevel >= 4) points *= MULTIPLIER_ADVANCED;\n'
            + '              u.rawScore += points;\n'
            + '\n'
            + '              skills.push({\n'
            + '                  name: sName,\n'
            + '                  avg_level: Math.round(avgLevel * 10) / 10,\n'
            + '                  entry_count: sg.entries.length,\n'
            + '                  entries: sg.entries,\n'
            + '                  isMust: sg.isMust\n'
            + '              });\n'
            + '          }\n'
            + '\n'
            + '          var matchPct = Math.min(Math.round((u.rawScore / maxPossible) * 100), 100);\n'
            + '          var names = u.name.split(" ");\n'
            + '          var initials = (names[0][0] + (names.length > 1 ? names[names.length-1][0] : "")).toUpperCase();\n'
            + '\n'
            + '          matches.push({\n'
            + '              id: u.sys_id,\n'
            + '              name: u.name,\n'
            + '              title: u.title,\n'
            + '              initials: initials,\n'
            + '              skills: skills,\n'
            + '              matchScore: matchPct,\n'
            + '              availability: "high"\n'
            + '          });\n'
            + '      }\n'
            + '\n'
            + '      matches.sort(function(a, b) { return b.matchScore - a.matchScore; });\n'
            + '      data.matches = matches;\n'
            + '  }\n'
            + '\n'
            + '  // --- ENDORSE ACTION ---\n'
            + '  if (input && input.action == "endorse") {\n'
            + '     var endorser = gs.getUserID();\n'
            + '     var skillRecId = input.skill_record_id;\n'
            + '     data.endorsed = false;\n'
            + '     data.endorseError = "";\n'
            + '\n'
            + '     if (!skillRecId) {\n'
            + '        data.endorseError = "No skill record specified.";\n'
            + '     } else {\n'
            + '        var skillRec = new GlideRecord("sys_user_has_skill");\n'
            + '        if (!skillRec.get(skillRecId)) {\n'
            + '           data.endorseError = "Skill record not found.";\n'
            + '        } else {\n'
            + '           var skillOwner = skillRec.user.toString();\n'
            + '           if (endorser == skillOwner) {\n'
            + '              data.endorseError = "You cannot endorse your own skill.";\n'
            + '           } else {\n'
            + '              var dup = new GlideRecord("u_m2m_skill_endorsement");\n'
            + '              dup.addQuery("u_endorser", endorser);\n'
            + '              dup.addQuery("u_skill_record", skillRecId);\n'
            + '              dup.query();\n'
            + '              if (dup.hasNext()) {\n'
            + '                 data.endorseError = "You have already endorsed this skill.";\n'
            + '              } else {\n'
            + '                 var endRec = new GlideRecord("u_m2m_skill_endorsement");\n'
            + '                 endRec.initialize();\n'
            + '                 endRec.u_endorser = endorser;\n'
            + '                 endRec.u_skill_record = skillRecId;\n'
            + '                 var newId = endRec.insert();\n'
            + '                 if (newId) {\n'
            + '                    data.endorsed = true;\n'
            + '                 } else {\n'
            + '                    data.endorseError = "Failed to create endorsement record.";\n'
            + '                 }\n'
            + '              }\n'
            + '           }\n'
            + '        }\n'
            + '     }\n'
            + '  }\n'
            + '})();\n';

        widgetGR = new GlideRecord('sp_widget');
        widgetGR.get(widgetId);
        widgetGR.setValue('script', newServer);
        widgetGR.update();
        gs.info('[Skills Hub] Server script replaced with name-based search + averaged scoring');

