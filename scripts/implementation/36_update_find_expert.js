            + '      for (var sn in nameToIds) {\n'
            + '          for (var i = 0; i < nameToIds[sn].length; i++) {\n'
            + '              allSkillIds.push(nameToIds[sn][i]);\n'
            + '              skillIdToName[nameToIds[sn][i]] = sn;\n'
            + '          }\n'
            + '      }\n'
            + '\n'
            + '      var users = {};\n'
            + '      if (allSkillIds.length > 0) {\n'
            + '          var gr = new GlideRecord("sys_user_has_skill");\n'
            + '          gr.addQuery("skill", "IN", allSkillIds.join(","));\n'
            + '          gr.addQuery("user.active", true);\n'
            + '          gr.query();\n'
            + '\n'
            + '          while (gr.next()) {\n'
            + '              var uid = gr.user.toString();\n'
            + '              var skillId = gr.skill.toString();\n'
