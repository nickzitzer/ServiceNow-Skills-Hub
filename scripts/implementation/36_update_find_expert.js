        var newClient = 'function($scope, spUtil) {\n'
            + '  var c = this;\n'
            + '  c.mustInput = "";\n'
            + '  c.niceInput = "";\n'
            + '  c.mustSuggestions = [];\n'
            + '  c.niceSuggestions = [];\n'
            + '  c.mustChips = [];\n'
            + '  c.niceChips = [];\n'
            + '  c.searched = false;\n'
            + '  c.loading = false;\n'
            + '  c.results = [];\n'
            + '\n'
            + '  var searchTimer = null;\n'
            + '\n'
            + '  c.typeaheadSearch = function(which) {\n'
            + '    var term = (which == "must") ? c.mustInput : c.niceInput;\n'
            + '    if (searchTimer) clearTimeout(searchTimer);\n'
            + '    if (!term || term.length < 2) {\n'
            + '      if (which == "must") c.mustSuggestions = [];\n'
            + '      else c.niceSuggestions = [];\n'
            + '      return;\n'
            + '    }\n'
            + '    searchTimer = setTimeout(function() {\n'
            + '      c.server.get({ action: "searchSkills", term: term }).then(function(r) {\n'
            + '        var list = r.data.skillSuggestions || [];\n'
            + '        // Filter out already-selected chips\n'
            + '        var existing = c.mustChips.concat(c.niceChips);\n'
            + '        var filtered = [];\n'
            + '        for (var i = 0; i < list.length; i++) {\n'
            + '          if (existing.indexOf(list[i]) == -1) filtered.push(list[i]);\n'
            + '        }\n'
            + '        if (which == "must") c.mustSuggestions = filtered;\n'
            + '        else c.niceSuggestions = filtered;\n'
            + '        $scope.$applyAsync();\n'
            + '      });\n'
            + '    }, 300);\n'
            + '  };\n'
            + '\n'
            + '  c.selectSkill = function(which, name) {\n'
            + '    if (which == "must") {\n'
            + '      if (c.mustChips.indexOf(name) == -1) c.mustChips.push(name);\n'
            + '      c.mustInput = "";\n'
            + '      c.mustSuggestions = [];\n'
            + '    } else {\n'
            + '      if (c.niceChips.indexOf(name) == -1) c.niceChips.push(name);\n'
            + '      c.niceInput = "";\n'
            + '      c.niceSuggestions = [];\n'
            + '    }\n'
            + '  };\n'
            + '\n'
            + '  c.removeChip = function(which, idx) {\n'
            + '    if (which == "must") c.mustChips.splice(idx, 1);\n'
            + '    else c.niceChips.splice(idx, 1);\n'
            + '  };\n'
            + '\n'
            + '  c.search = function() {\n'
            + '    if (c.mustChips.length == 0 && c.niceChips.length == 0) return;\n'
            + '    c.loading = true;\n'
            + '    c.searched = true;\n'
            + '    c.results = [];\n'
            + '    c.server.get({\n'
            + '      action: "search",\n'
            + '      mustHave: c.mustChips.join(","),\n'
            + '      niceHave: c.niceChips.join(",")\n'
